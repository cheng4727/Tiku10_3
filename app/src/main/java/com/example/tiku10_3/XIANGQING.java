package com.example.tiku10_3;

public class XIANGQING {
    private int ch,renshu;

    public XIANGQING(int ch, int renshu) {
        this.ch = ch;
        this.renshu = renshu;
    }

    public int getCh() {
        return ch;
    }

    public void setCh(int ch) {
        this.ch = ch;
    }

    public int getRenshu() {
        return renshu;
    }


    public void setRenshu(int renshu) {
        this.renshu = renshu;
    }
}
