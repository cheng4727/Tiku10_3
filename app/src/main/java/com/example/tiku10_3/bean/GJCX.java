package com.example.tiku10_3.bean;

public class GJCX {
    private int id,renshu,juli;

    public GJCX(int id, int renshu, int juli) {
        this.id = id;
        this.renshu = renshu;
        this.juli = juli;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRenshu() {
        return renshu;
    }

    public void setRenshu(int renshu) {
        this.renshu = renshu;
    }

    public int getJuli() {
        return juli;
    }

    public void setJuli(int juli) {
        this.juli = juli;
    }
}
